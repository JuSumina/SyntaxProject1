package class4;

public class IfElseConditions {
    public static void main(String[] args) {

        boolean rain = true;

        if (rain) {
            System.out.println("Lets bring umbrella with us");
        } else {
            System.out.println("Let's bring a hat");
        }

        boolean breaktime = true;

        if (breaktime){
            System.out.println("Let's take a break till 11:50");
        }
    }
}
