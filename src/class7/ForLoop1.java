package class7;

public class ForLoop1 {

    public static void main(String[] args) {

        int num1=0;

        while (num1<10) {
            System.out.println(num1);
            num1++;
        }

        for (int num2=0; num2<10; num2++){

            System.out.println(num2);

        }

    }
}
