package class6;

public class IncrementOperator1 {

    public static void main(String[] args) {

        int number=10;
        number=number+1;
        System.out.println(number);
        number+=1;
        System.out.println(number);
        number++;


        int number2=10;
        number2--;
        System.out.println(number2);
    }
}
