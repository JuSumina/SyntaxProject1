package class6;

public class WhileLoop2 {

    public static void main(String[] args) {

        int num3=1;
        while (num3<=100) {
            System.out.println(num3);
            num3++;
        }

        int num4=1;
        while (num4<=15) {
            System.out.println(num4);
            num4+=2;
        }

        int num5=5;
        while (num5<=50) {
            System.out.println(num5);
            num5+=5;
        }

        int num6=10;
        while (num6>0) {
            System.out.println(num6);
            num6--;

        }

    }
}
