package class3;

public class IfElse9 {

    public static void main(String[] args) {

        char c='F';
        if(c=='M'){
            System.out.println("Male");
        }

        if(c=='F'){
            System.out.println("Female");
        }

        String name="Jane";

        if(name.equals("Sam")){
            System.out.println("Amazing student");
        }

        if(!name.equals("Sam")){
            System.out.println("Great student");
        }
    }
}
